# Se define un Tuple con distintos datos (string, int y booleano en este caso)
carro = ("Ferrari", 90, true)

# Con el indice se accede a los elementos
println("Primer elemento: ", carro[1]) #en este caso puede ser la marca del carro
println("Segundo elemento: ", carro[2]) # en este caso puede ser el modelo del carro
println("tercer elemento: ", carro[3]) # en este caso puede ser si es modificado o no el carro



# Se define un string se puede declarar antes o no con el operador ::
